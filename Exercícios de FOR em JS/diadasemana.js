let num = Number(prompt('Digite um número de 1 a 7 para saber o dia da semana: '))

switch(num){
    case 1:
        console.log('Na semana o dia 1 é o DOMINGO!')
        break;
    case 2:
        console.log('Na semana o dia 2 é o SEGUNDA!')
        break;
    case 3:
        console.log('Na semana o dia 3 é o TERÇA!')
        break;
    case 4:
        console.log('Na semana o dia 4 é o QUARTA!')
        break;
    case 5:
        console.log('Na semana o dia 5 é o QUINTA!')
        break;
    case 6:
        console.log('Na semana o dia 6 é o SEXTA!')
        break;
    default:
        console.log('Na semana o dia 7 é o SÁBADO!')
        break;       
}




