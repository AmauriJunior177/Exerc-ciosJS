let diaSemana = Number(prompt("Digite um número de 1 a 7 para saber o dia da semana: "))

switch(diaSemana){
    case 1:
        console.log('O dia 1 da semana é DOMINGO')
        break;
    case 2:
        console.log('O dia 2 da semana é SEGUNDA-FEIRA')
        break;
    case 3:
        console.log('O dia 3 da semana é TERÇA-FEIRA')
        break;
    case 4:
        console.log('O dia 4 da semana é QUARTA-FEIRA')
        break;
    case 5:
        console.log('O dia 5 da semana é QUINTA-FEIRA')
        break;
    case 6:
        console.log('O dia 6 da semana é SEXTA-FEIRA')
        break;
    case 7:
            console.log('O dia 7 da semana é SABADO')
            break;
    default:
            console.log('Número inválido')
            break; 
}









